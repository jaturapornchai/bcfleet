# Parse ID from JSON response — tries jq first, falls back to python, then grep
parse_id() {
  local json="$1"
  local id=""
  if command -v jq &>/dev/null; then
    id=$(echo "$json" | jq -r '.id // .data.id // ._id // .data._id // empty' 2>/dev/null)
  fi
  if [ -z "$id" ] && command -v python3 &>/dev/null; then
    id=$(python3 -c "
import json,sys
d=json.loads(sys.stdin.read())
for k in ['id','_id']:
    if k in d: print(d[k]); break
    elif 'data' in d and k in d['data']: print(d['data'][k]); break
" <<< "$json" 2>/dev/null)
  fi
  if [ -z "$id" ]; then
    id=$(echo "$json" | grep -oP '"(?:id|_id)"\s*:\s*"([^"]+)"' | head -1 | grep -oP ':\s*"\K[^"]+')
  fi
  echo "$id"
}

post_json() {
  local endpoint="$1"
  local data="$2"
  local resp
  resp=$(curl -s -w "\n%{http_code}" -X POST "${API_BASE}${endpoint}" \
    -H "Content-Type: application/json" \
    -d "$data" 2>/dev/null) || true
  local http_code
  http_code=$(echo "$resp" | tail -1)
  local body
  body=$(echo "$resp" | sed '$d')
  if [[ "$http_code" =~ ^2 ]]; then
    echo "$body"
  else
    echo "ERROR($http_code): $body" >&2
    ((FAILED++)) || true
    echo ""
  fi
}

put_json() {
  local endpoint="$1"
  local data="$2"
  curl -s -X PUT "${API_BASE}${endpoint}" \
    -H "Content-Type: application/json" \
    -d "$data" 2>/dev/null || true
}

