create_vehicle() {
  local plate="$1" brand="$2" model="$3" vtype="$4" year="$5" color="$6"
  local chassis="$7" engine="$8" fuel="$9" weight="${10}" ownership="${11}" mileage="${12}"
  local json
  json=$(cat <<EOJSON
{"plate":"${plate}","brand":"${brand}","model":"${model}","type":"${vtype}","year":${year},"color":"${color}","chassis_no":"${chassis}","engine_no":"${engine}","fuel_type":"${fuel}","max_weight_kg":${weight},"ownership":"${ownership}","mileage_km":${mileage}}
EOJSON
)
  local resp
  resp=$(post_json "/vehicles" "$json")
  local id
  id=$(parse_id "$resp")
  if [ -n "$id" ]; then
    VEHICLE_IDS+=("$id")
    ((CREATED_VEHICLES++))
    echo "  [${CREATED_VEHICLES}] Vehicle ${plate} -> ${id}"
  else
    echo "  [FAIL] Vehicle ${plate}" >&2
  fi
}

