# Helper: get vehicle/driver ID by index (wraps around)
vid() {
  local idx=$1
  local len=${#VEHICLE_IDS[@]}
  if [ "$len" -eq 0 ]; then echo "no-vehicle"; return; fi
  echo "${VEHICLE_IDS[$((idx % len))]}"
}
did() {
  local idx=$1
  local len=${#DRIVER_IDS[@]}
  if [ "$len" -eq 0 ]; then echo "no-driver"; return; fi
  echo "${DRIVER_IDS[$((idx % len))]}"
}

create_trip() {
  local v_idx="$1" d_idx="$2"
  local o_name="$3" o_addr="$4" o_lat="$5" o_lng="$6" o_contact="$7" o_phone="$8"
  local d_name="$9" d_addr="${10}" d_lat="${11}" d_lng="${12}" d_contact="${13}" d_phone="${14}"
  local cargo="${15}" weight="${16}"
  local p_start="${17}" p_end="${18}" revenue="${19}"

  local v_id
  v_id=$(vid "$v_idx")
  local dr_id
  dr_id=$(did "$d_idx")

  local json
  json=$(cat <<EOJSON
{"vehicle_id":"${v_id}","driver_id":"${dr_id}","origin":{"name":"${o_name}","address":"${o_addr}","lat":${o_lat},"lng":${o_lng},"contact_name":"${o_contact}","contact_phone":"${o_phone}"},"destinations":[{"seq":1,"name":"${d_name}","address":"${d_addr}","lat":${d_lat},"lng":${d_lng},"contact_name":"${d_contact}","contact_phone":"${d_phone}"}],"cargo":{"description":"${cargo}","weight_kg":${weight}},"planned_start":"${p_start}","planned_end":"${p_end}","revenue":${revenue}}
EOJSON
)
  local resp
  resp=$(post_json "/trips" "$json")
  local id
  id=$(parse_id "$resp")
  if [ -n "$id" ]; then
    TRIP_IDS+=("$id")
    ((CREATED_TRIPS++))
    echo "  [${CREATED_TRIPS}] Trip ${o_addr}->${d_addr} -> ${id}"
  else
    echo "  [FAIL] Trip ${o_addr}->${d_addr}" >&2
  fi
}

